# type() => to get any data type of a variable.

s = "hello world"
print(type(s))  # <class 'str'>

print(type(100))  # <class 'int'>
print(type(True))  # <class 'bool'>
# Attention True and False starts with capital letters.

print(type(None))  # <class 'NoneType'> Java'daki "null" gibi.
print(type(7/3))  # <class 'float'>
print(type(6/2))  # <class 'float'>

